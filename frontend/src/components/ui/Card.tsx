import { cn } from "@/lib/utils";
import { forwardRef } from "react";

const Card = forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-default bg-primary-card p-6 shadow-sm",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

